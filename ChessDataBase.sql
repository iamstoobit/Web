CREATE TABLE USERS (
    ID INT NOT NULL PRIMARY KEY GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1),
    EMAIL VARCHAR(50) UNIQUE NOT NULL,
    USERNAME VARCHAR(50) NOT NULL,
    PASSWORD VARCHAR(50) NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    ELO INT NOT NULL DEFAULT 300
);

CREATE TABLE GAMES (
    GAME_ID INT NOT NULL PRIMARY KEY GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1),
    PLAYER_ID_WHITE INT NOT NULL,
    PLAYER_ID_BLACK INT NOT NULL,
    DATE_TIME_PLAYED TIMESTAMP NOT NULL,
    RESULT VARCHAR(7) NOT NULL,  -- 3 possibilities: 1-0(white won), 0-1(black won), 1/2-1/2(draw)
    TOURNAMENT VARCHAR(50) DEFAULT 'N/A'
);

ALTER TABLE GAMES
ADD FOREIGN KEY(PLAYER_ID_WHITE) REFERENCES USERS(ID)
ADD FOREIGN KEY(PLAYER_ID_BLACK) REFERENCES USERS(ID);

CREATE TABLE MOVES (
    GAME_ID INT NOT NULL,
    MOVE VARCHAR(7) NOT NULL, --in algebraic chess notation - 'c4; exd5; Bc7; c8=Q+; O-O-O; R4a5; Qh4xe3#'
    TURN INT NOT NULL
);

ALTER TABLE MOVES
ADD FOREIGN KEY(GAME_ID) REFERENCES GAMES(GAME_ID);

CREATE TABLE TOURNAMENTS (
    TOURNAMENT_ID INT NOT NULL PRIMARY KEY GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1),
    TOURNAMENT_NAME VARCHAR(50) NOT NULL DEFAULT 'UNKNOWN',
    PLAYER_ID INT NOT NULL
);

ALTER TABLE TOURNAMENTS
ADD FOREIGN KEY(PLAYER_ID) REFERENCES USERS(ID);

